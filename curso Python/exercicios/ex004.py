alguma_coisa = input('Digite algo ')
print(type(alguma_coisa))
print('È númerico? ' , alguma_coisa.isalnum())
print('È alfabético? ' , alguma_coisa.isalpha())
print('Possui apenas letras minusculas? ' , alguma_coisa.islower())
print('È um número decimal? ' , alguma_coisa.isdecimal())
print(alguma_coisa.isnumeric())
print(alguma_coisa.isspace())
print(alguma_coisa.isupper())


