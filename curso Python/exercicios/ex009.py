print(f'A tabuada de divisão desse número é: \n{numero/1}\n{numero/2}\n{numero/3}\n{numero/4}\n{numero/5}\n{numero/6}\n{numero/7}\n{numero/8}\n{numero/9}\n{numero/10}')

print(f'A tabuada de subtração desse número é: \n{numero-1}\n{numero-2}\n{numero-3}\n{numero-4}\n{numero-5}\n{numero-6}\n{numero-7}\n{numero-8}\n{numero-9}\n{numero-10}')